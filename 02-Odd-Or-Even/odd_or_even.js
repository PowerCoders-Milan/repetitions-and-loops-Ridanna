// write a for loop to iterate from 0 to 20

  // check if the current is even or odd

  // report the result to the screen


for (i = 0; i < 21; i++) {
  if (i === 0) {
    console.log(i + " is even");
  } else if (i % 2 === 0) {
    console.log(i + " is even");
  } else {
    console.log(i + " is odd");
  }
}

